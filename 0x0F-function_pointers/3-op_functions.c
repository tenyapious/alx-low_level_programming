#include <stdio.h>

/**
 * op_add - add 2 numbers
 * @num1: first number
 * @num2: second number
 *
 * Return: result of num1 + num2
*/
int op_add(int num1, int num2)
{
	return (num1 + num2);
}

/**
 * op_sub - substract 1 number from another
 * @num1: first number
 * @num2: second number
 *
 * Return:	result of num1 - num2
*/
int op_sub(int num1, int num2)
{
	return (num1 - num2);
}

/**
 * op_mul - multiply 2 numbers
 * @num1: first number
 * @num2: second number
 *
 * Return: product of 2 numbers
*/
int op_mul(int num1, int num2)
{
	return (num1 * num2);
}

/**
 * op_div - divide one number from another
 * @num1: first number
 * @num2: second number
 *
 * Return: result of num1 / num2
*/
int op_div(int num1, int num2)
{
	return (num1 / num2);
}

/**
 * op_mod - find modulo of a number
 * @num1: first number
 * @num2: second number
 *
 * Return: result of num1 % num2
*/
int op_mod(int num1, int num2)
{
	return (num1 % num2);
}
